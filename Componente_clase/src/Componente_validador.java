      public void validar()
{
    switch(tipo_validacion)
    {  
         case "telefono": 
            if(jtextcomponente.getText().isEmpty()){
                jlabelerror.setText("no puede estar vacio");
            }else if(jtextcomponente.getText().length()==9)
            {
                jlabelerror.setText("tienen que ser 9 numeros");
            }else{jlabelerror.setText("todo correcto");
                  validador = true;
            }
            break;
        
          case "DNI": 
            if(jtextcomponente.getText().isEmpty()){
                jlabelerror.setText("no puede estar vacio");
            }else if(jtextcomponente.getText().length()==9)
            {
                jlabelerror.setText("tienen que ser 9 numeros");
            }else{jlabelerror.setText("todo correcto");
                validador = true;
            }
            break;
            
    }
}
